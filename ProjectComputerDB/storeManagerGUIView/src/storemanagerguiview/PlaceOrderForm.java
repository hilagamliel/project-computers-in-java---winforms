    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        try {
            if (comoboxCustomer.getSize() == 0) {
                throw new MyExpetion("הכנס לקוח!");
            }
            if (SelectedProductsListModel.getSize() == 0) {
                throw new MyExpetion("הכנס מוצרים!");
            }
            Customer c = (Customer) comoboxCustomer.getSelectedItem();
            ArrayList<Product> plist = new ArrayList<>();
            Enumeration<Product> elements = SelectedProductsListModel.elements();
            while (elements.hasMoreElements()) {
                Product nextElement = elements.nextElement();
                plist.add(nextElement);
            }
            PurchaseOrder Porder = new PurchaseOrder(c, plist,LocalDate.now());
            Backend_DAO_DB.get_bdl_singleton().PlaceOrder(Porder);
            JOptionPane.showMessageDialog(PlaceOrderForm.this, "ההזמנה נוספה בהצלחה", "הודעה", JOptionPane.HEIGHT);

        } catch (MyExpetion ex) {
            JOptionPane.showMessageDialog(PlaceOrderForm.this, ex.getMessage(), "!שגיאה", JOptionPane.HEIGHT);
        } catch (Exception ex) {
            Logger.getLogger(PlaceOrderForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

