    public void refreshCustomer() {
        try {
            Collection<Customer> CustomersComobox = Backend_DAO_DB.get_bdl_singleton()
                    .getAllCustomers().values();
            Customer[] c = CustomersComobox.toArray(new Customer[CustomersComobox.toArray().length]);
            comoboxCustomer.removeAllElements();
            for (Customer customer : c) {
                this.comoboxCustomer.addElement(customer);
            }
        } catch (Exception ex) {
            Logger.getLogger(PlaceOrderForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

